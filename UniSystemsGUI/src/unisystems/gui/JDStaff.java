    private void btnDeleteStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteStaffActionPerformed
        for (Staff staff : UniSystems.getInstance().getStaffList()){
             if ((staff.getiD().equals(iD))){
                 this.parent.deleteObject(staff);
             }
        }
        this.parent.updateStaffMember(UniSystems.getInstance().getStaffList(), this.parent.getLstStaff());
        this.dispose();
    }//GEN-LAST:event_btnDeleteStaffActionPerformed


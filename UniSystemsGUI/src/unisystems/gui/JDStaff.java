    private void btnSaveEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveEditActionPerformed
        try {
            validateInputs(this.pnlEditStaff);
            StaffMember oldStaff = new StaffMember();
            StaffMember newStaff = new StaffMember();
            for (Staff staff : UniSystems.getInstance().getStaffList()) {
                if (staff.getiD().equals(this.iD)){
                    oldStaff = new StaffMember(staff.getiD(), staff.getName(), staff.getAddress(), staff.getVehicleAccessGroup());
                    newStaff = new StaffMember(staff.getiD(), this.txtEditName.getText(), this.txtEditAddress.getText(), (VehicleAccessGroup) this.model.getSelectedItem());
                    this.parent.editObject(oldStaff, newStaff);
                    break;
                }
            }
            this.parent.updateStaffMember(UniSystems.getInstance().getStaffList(), this.parent.getLstStaff());
            this.dispose();
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Input error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveEditActionPerformed


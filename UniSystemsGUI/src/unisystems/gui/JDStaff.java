    public Component[] getInputFields(JPanel panel) {
        if (panel == pnlAddStaff){
            Component[] inputFields = {this.txtAddName, this.txtAddAddress};
            return inputFields;
        } else if (panel == pnlEditStaff){
            Component[] inputFields = {this.txtEditName, this.txtEditAddress};
            return inputFields;
        }
        return null;
    }
    
    public void validateInputs(JPanel panel) throws Exception {
        //validate code
        Boolean error = false;
        String errorMessage = "";
        //Object[] inputFields = new Object[5];
        
        Component[] inputFields = getInputFields(panel);
        
        if ("".equals(((javax.swing.JTextField)inputFields[0]).getText())){
            error = true;
            errorMessage = "No name entered.";
        } else if ("".equals(((javax.swing.JTextArea)inputFields[1]).getText())) {
            error = true;
            errorMessage = "No address entered.";
        }
        
        if (error) {
            throw new Exception(errorMessage);
        }
    }
    

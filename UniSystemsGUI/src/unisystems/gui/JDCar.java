    /**
     * Adds a service to the car currently being edited. Ensures that there is not
     * already an open service for the car. Before allowing the user to pick a service
     * date it offers to automatically set the service for today.
     * @param evt 
     */
    private void btnAddServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddServiceActionPerformed
        try {
            LocalDate date = LocalDate.now();
            for (Car car : UniSystems.getInstance().getCarList()){
                if (registration.equals(car.getRegistration())){
                    Boolean doDatesClash = false;
                    
                    for (Service service : car.getServiceHistory()) {
                        if (service.doDatesClash(date, date)) {
                            doDatesClash = true;
                            break;
                        }
                    }
                    
                    if (doDatesClash){
                        JOptionPane.showMessageDialog(this, "The vehicle is booked to be serviced.", "Dates clash", JOptionPane.ERROR_MESSAGE);
                    } else {
                        int messageDialog = JOptionPane.showConfirmDialog(this, "Send this car for service today?", "Book Service",JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        if (messageDialog == JOptionPane.YES_OPTION) {
                            date = LocalDate.now();
                        } else if (messageDialog == JOptionPane.NO_OPTION) {
                            org.jdesktop.swingx.JXDatePicker datePicker = new org.jdesktop.swingx.JXDatePicker();
                            datePicker.setDate(Date.from(LocalDate.now().plus(1, ChronoUnit.DAYS).atStartOfDay(ZoneId.systemDefault()).toInstant()));
                            Object[] params = {"Select Date: ", datePicker};
                            int value = JOptionPane.showConfirmDialog(this, params, "Date", JOptionPane.OK_CANCEL_OPTION);
                            
                            if (value == JOptionPane.OK_OPTION) {
                                Date dateSelected = ((org.jdesktop.swingx.JXDatePicker)params[1]).getDate();
                                date = dateSelected.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
                            }    
                        }
                        car.addService(new Service(date));
                    }
                }
            }
        } catch (NullPointerException ex){
            JOptionPane.showMessageDialog(this, "A service date must be entered.", "Input error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddServiceActionPerformed


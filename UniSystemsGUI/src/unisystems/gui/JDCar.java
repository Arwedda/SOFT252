    /**
     * Calls the validateInputs method then reassigns the selected car with 
     * field details entered by the user then disposes of the JDialog.
     * Finds the car based on the locally saved registration variable relating 
     * to the loaded car (this allows the user to edit the registration without issues).
     * It also deletes and recreates the car if the car is moved from one loan type to the other.
     * @param evt 
     */
    private void btnSaveEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveEditActionPerformed
        try {
            validateInputs(this.pnlEditCar);
            String reg = this.txtEditRegistration.getText();
            String make = this.txtEditMake.getText();
            String modl = this.txtEditModel.getText();
            Integer mileage = Integer.parseInt(this.txtEditMileage.getText());
            TypeOfVehicle typeOfVehicle = (TypeOfVehicle) this.model.getSelectedItem();
            Date pickedDate = this.dpEditServiceDue.getDate();
            LocalDate serviceDue = pickedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            for (Car car : UniSystems.getInstance().getCarList()){
                if (car.getRegistration().equals(this.registration)){
                    Car oldCar = new ShortTermCar();
                    if (car instanceof LongTermCar) {
                        oldCar = new LongTermCar(car.getRegistration(), car.getMake(), car.getModel(), true, false, car.getMileage(), car.getServiceDue(), car.getTypeOfVehicle(), false);
                    } else if (car instanceof ShortTermCar) {
                        oldCar = new ShortTermCar(car.getRegistration(), car.getMake(), car.getModel(), true, false, car.getMileage(), car.getServiceDue(), car.getTypeOfVehicle(), false);
                    }
                    
                    if (car instanceof LongTermCar){
                        if (this.rdbEditCarShortTerm.isSelected()){
                            ShortTermCar newCar = new ShortTermCar(reg, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
                            this.parent.editObject(oldCar, newCar);
                        } else {
                            LongTermCar newCar = new LongTermCar(reg, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
                            this.parent.editObject(oldCar, newCar);
                        }
                    }else if (car instanceof ShortTermCar){
                        if (this.rdbEditCarLongTerm.isSelected()){
                            LongTermCar newCar = new LongTermCar(reg, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
                            this.parent.editObject(oldCar, newCar);
                        } else {
                            ShortTermCar newCar = new ShortTermCar(reg, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
                            this.parent.editObject(oldCar, newCar);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "This is not a Short Term Car or a Long Term Car.");
                    }
                    break;
                }
            }
            this.parent.updateCar(UniSystems.getInstance().getCarList(), this.parent.getLstCars());
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Input error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveEditActionPerformed
    

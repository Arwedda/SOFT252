    /**
     * Calls the validateInputs method then creates a new car with the
     * field details and adds it to the list of cars then disposes of the JDialog.
     * Picks which sub-class of car to create based on which radio button is selected.
     * @param evt 
     */
    private void btnAddCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCarActionPerformed
        try {
            validateInputs(this.pnlAddCar);
            Car newCar;
            String registration = this.txtAddRegistration.getText();
            String make = this.txtAddMake.getText();
            String modl = this.txtAddModel.getText();
            Integer mileage = Integer.parseInt(this.txtAddMileage.getText());
            TypeOfVehicle typeOfVehicle = (TypeOfVehicle) this.model.getSelectedItem();
            LocalDate serviceDue = LocalDate.now().plusYears(1);

            if (this.rdbAddLongTerm.isSelected()){
                newCar = new LongTermCar(registration, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
            }else{
                newCar = new ShortTermCar(registration, make, modl, true, false, mileage, serviceDue, typeOfVehicle, false);
            }
            this.parent.addCar(newCar);

            this.parent.updateCar(UniSystems.getInstance().getCarList(), this.parent.getLstCars());
            this.dispose();
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Input error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddCarActionPerformed


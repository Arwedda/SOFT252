    /**
     * Checks which panel we're building the array on the creates as array of components from inputs on the form then returns the array
     * @param panel
     * @return The array of components
     */
    public Component[] getInputFields(JPanel panel) {
        if (panel == pnlAddCar){
            Component[] inputFields = {this.txtAddRegistration, this.txtAddMake, this.txtAddModel, this.txtAddMileage};
            return inputFields;
        } else if (panel == pnlEditCar){
            Component[] inputFields = {this.txtEditRegistration, this.txtEditMake, this.txtEditModel, this.txtEditMileage, this.dpEditServiceDue};
            return inputFields;
        }
        return null;
    }
    
    /**
     * Assesses fields input by the user against pre-specified criteria such as fields being required and
     * format requirements
     * @param panel The panel that the user is currently on
     * @throws Exception exception based on input errors created by the user
     */
    public void validateInputs(JPanel panel) throws Exception {
        //validate code
        Boolean error = false;
        String errorMessage = "";
        
        Component[] inputFields = getInputFields(panel);
        
        for (Car car : UniSystems.getInstance().getCarList()) {
            if (!car.getRegistration().equals(this.registration)) {
                if (((javax.swing.JTextField)inputFields[0]).getText().equals(car.getRegistration())) {
                    error = true;
                    errorMessage = "Registration must be unique";
                }
            }
        }
        
        if ("".equals(((javax.swing.JTextField)inputFields[0]).getText())){
            error = true;
            errorMessage = "No registration entered.";
        } else if ("".equals(((javax.swing.JTextField)inputFields[1]).getText())) {
            error = true;
            errorMessage = "No make entered.";
        } else if ("".equals(((javax.swing.JTextField)inputFields[2]).getText())) {
            error = true;
            errorMessage = "No model entered.";
        } else if ("".equals(((javax.swing.JTextField)inputFields[3]).getText())) {
            error = true;
            errorMessage = "No mileage entered.";
        } else if (!"".equals(((javax.swing.JTextField)inputFields[3]).getText())){
            try {
                int a;
                a = Integer.parseInt(((javax.swing.JTextField)inputFields[3]).getText());              
            } catch (NumberFormatException ex){
                error = true;
                errorMessage = "Non-numeric characters entered into mileage field.";
            }
        }
        
        try {
            Instant today = LocalDate.now().plus(1, ChronoUnit.DAYS).atStartOfDay(ZoneId.systemDefault()).toInstant();
            if (((org.jdesktop.swingx.JXDatePicker)inputFields[4]).getDate() == null){
                error = true;
                errorMessage = "Date for next service must be entered.";
            } else if (((org.jdesktop.swingx.JXDatePicker)inputFields[4]).getDate().before(Date.from(today))) {
                error = true;
                errorMessage = "Date for next service must be after today's date.";
            }
        } catch (IndexOutOfBoundsException ex) {

        }
        if (error) {
            throw new Exception(errorMessage);
        }
    }
    
    /**
     * Finds any service relating to the car currently being edited
     * @return returns the open service
     */
    private Service getService(){
        Service foundService = null;
        for (Car car : UniSystems.getInstance().getCarList()){
            if (registration.equals(car.getRegistration())){
                if (car.getServiceHistory().getServiceHistory() != null) { //.getServiceHistory required twice due to itterator interface
                    for (Service service : car.getServiceHistory()){
                        if (service.getReturnDate() == null){
                            foundService = service;
                        }
                    } 
                }
            }
        }
        return foundService;
    }
    
    /**
     * Shows passed car on the edit car panel
     * @param car car to be edited
     */
    public void showCar(Car car) {
        LocalDate localDate = car.getServiceDue();
        Date serviceDue = Date.from(localDate.atStartOfDay(ZoneId.systemDefault()).toInstant());
        this.registration = car.getRegistration();
        this.txtEditRegistration.setText(this.registration);
        this.txtEditMake.setText(car.getMake());
        this.txtEditModel.setText(car.getModel());
        this.txtEditMileage.setText(car.getMileage().toString());
        this.cmbEditTypeOfVehicle.setSelectedItem(car.getTypeOfVehicle());
        this.dpEditServiceDue.setDate(serviceDue);
        
        if (car instanceof LongTermCar){
            this.rdbEditCarLongTerm.setSelected(true);
        } else if (car instanceof ShortTermCar) {
            this.rdbEditCarShortTerm.setSelected(true);
        } else {
            JOptionPane.showMessageDialog(null, "This is not a Short Term Car or a Long Term Car.");
        }
    }
    

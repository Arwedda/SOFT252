    /**
     * @param panel. A string that determines which panel to add
     * Case statement that determines which panel to display
     */
    public void setPanel(String panel){
        switch (panel){
            case "add":
                changePanel(this.pnlAddCar);
                this.lblServiceDue.setText(LocalDate.now().plusYears(1).toString());
                break;
            case "edit":
                changePanel(this.pnlEditCar);
                break;
            case "delete":
                changePanel(this.pnlDeleteCar);
        }
    }
    
    /**
     * @param panel The panel to be displayed
     * Removes panels then displays the new panel
     */
    private void changePanel(JPanel panel) {
        getContentPane().removeAll();
        getContentPane().add(panel, BorderLayout.CENTER);
        getContentPane().doLayout();
        update(getGraphics());
    }
    
